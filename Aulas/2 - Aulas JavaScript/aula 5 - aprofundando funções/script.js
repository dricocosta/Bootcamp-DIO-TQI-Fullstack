// tipos de funções

// declarativas
function funcao() {
    console.log('Tudo certo, jovem!')
}

funcao();

// expressão de função com nomeação (opcional)
var funcao = function funcao() {
    console.log('Sou uma mensagem de função de expressão!')
}
funcao();

// arrow function
var funcao = () => {
    console.log('Sou uma arrow function.')
}
funcao();

